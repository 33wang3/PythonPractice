def number_countries(a):
    b=[]
    d={}
    for i in a:
       b.append(i[0])
    c=set(b)
    for j in c:
        e=b.count(j)
        d[j]=e
    return d

print(number_countries(countries_list))

def number_countries_fengchang(countries):
    cnt = {}
    for country in countries:
        if country[0] not in cnt:
            cnt[country[0]] = 1
        else:
            cnt[country[0]] += 1
    return cnt

